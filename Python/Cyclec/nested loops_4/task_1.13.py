# –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∏–∑ –æ—Ç—Ä–µ–∑–∫–∞ [a;b] —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π.

# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
#
# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –¥–≤–∞ —á–∏—Å–ª–∞ –Ω–∞ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ, —Ä–∞–∑–¥–µ–ª–µ–Ω–Ω—ã—Ö –ø—Ä–æ–±–µ–ª–æ–º: —á–∏—Å–ª–æ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π –∏
# —Å—É–º–º—É –µ–≥–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π.
#
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —á–∏—Å–µ–ª –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∏–∑ –Ω–∏—Ö.

a = int(input())
b = int(input())
counter = 0                           # —Å—á–µ—Ç—á–∏–∫ –¥–ª—è –∫–æ–Ω–µ—á–Ω–æ–≥–æ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏—è —Å—É–º–º—ã
largest = 0                           # –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è –ø—Ä–∏–Ω—è—Ç–∏—è —Ç–æ–≥–æ —Å–∞–º–æ–≥–æ —á–∏—Å–ª–∞ —É –∫–æ—Ç–æ—Ä–æ–≥–æ —Å—É–º–º–∞ –¥–µ–ª–∏—Ç–µ–ª–µ–π –±–æ–ª—å—à–µ
for i in range(a, b + 1):
    total = 0                         # —Å—á–µ—Ç—á–∏–∫ (–æ–±–Ω—É–ª—è—Ç–æ—Ä)
    for j in range(1, i + 1):
        if i % j == 0:
            total += j                # –ø–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º —Å—É–º–º—É –¥–µ–ª–∏—Ç–µ–ª–µ–π –∏–º–µ–Ω–Ω–æ —ç—Ç–æ–≥–æ —á–∏—Å–ª–∞
            if total >= counter and i >= largest:  # –µ—Å–ª–∏ —Å—É–º–º–∞ –±–æ–ª—å—à–µ –∫–æ–Ω–µ—á–Ω–æ–≥–æ —Å—á–µ—Ç—á–∏–∫–∞ and 'i' >= largest, –µ—Å–ª–∏ —á–∏—Å–ª–æ –Ω–∞ –∫–æ—Ç–æ—Ä–æ–µ –º—ã –¥–µ–ª–∏–º –±–æ–ª—å—à–µ –∫–æ–Ω–µ—á–Ω–æ–≥–æ —á–∏—Å–ª–∞
                counter = total
                largest = i
print('–ß–∏—Å–ª–æ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π = ', largest)
print('–°—É–º–º–∞ –¥–µ–ª–∏—Ç–µ–ª–µ–π', counter)

