# Наибольшие числа 🌶️🌶️
# На вход программе подается натуральное число n, а затем nn различных натуральных чисел, каждое на отдельной строке.
# Напишите программу, которая выводит наибольшее и второе наибольшее число последовательности.
#
# Формат входных данных
# На вход программе подаются натуральное число n, n ≥ 2, а затем n различных натуральных чисел, каждое на отдельной строке.
#
# Формат выходных данных
# Программа должна вывести два наибольших числа, каждое на отдельной строке.
n = int(input())
largest_max = 0
largest_next = 0
for i in range(n):
    num = int(input())
    if num > largest_max:
        largest_next = largest_max
        largest_max = num
    elif largest_next < num < largest_max:
        largest_next = num
print(largest_max)
print(largest_next)