def is_valid_password(password):
    password = password.split(':')
    a = password[0]
    b = int(password[1])
    c = int(password[2])
    flag1 = False
    flag2 = False
    flag3 = False
    flag4 = False
    if len(password) == 3:
        flag1 = True
    if a[:] == a[::-1]:
        flag2 = True
    for i in range(2, b):
        if b % i == 0:
            break
    else:
        flag3 = True
    if c % 2 == 0:
        flag4 = True
    return flag1 and flag2 and flag3 and flag4
