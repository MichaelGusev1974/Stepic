def is_pangram(text):
    counter = 0
    abc = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u',
           'v', 'w', 'x', 'y', 'z']
    for i in abc:
        if i in list(text.lower()):
            counter += 1
    if counter == 26:
        return True
    else:
        return False

