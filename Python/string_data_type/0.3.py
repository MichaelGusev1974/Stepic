a = input()
b = input()
c = input()
a1 = len(a)
b1 = len(b)
c1 = len(c)
min_s = min(a1, b1, c1)
max_s = max(a1, b1, c1)
av_s = (a1 + b1 + c1) - (max_s + min_s)

if (max_s - av_s) == av_s - min_s:
    print('YES')
else:
    print('NO')